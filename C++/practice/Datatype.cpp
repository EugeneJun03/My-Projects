/*There are Big 4 data types in C++
1.int: 1, 2, 3
2.double: 3.14
3.char: 'A', 'B'
4.bool: 'true' or 'false'
 */

#include <iostream>
using namespace std;

int main() {
    int age = 25;
    double height = 1.83;
    char grade = 'A';
    bool isStudent = true;

    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;
    cout << "Is student " << isStudent << endl;

    return 0;
}